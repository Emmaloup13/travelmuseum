<template>
    <div id="home">
        <a id="lien" v-if="click" :href="pageRegion">Voir les mus√©es de {{ regionName }} !</a>
    </div>
</template>

<script>

import getData from '@/functions/getMuseumData.js';
import { store } from '../../functions/store';

export default {
    name: "MyHome",
    data() {
        return {
            data: {},
            store,
        }
    },
    mounted() {
        this.data = getData.findAll();
    },
    computed: {
        regionName() {
            return JSON.parse(JSON.stringify(this.store.region_name));
        },
        click() {
            if (this.regionName != "") {
                return true;
            }
            else {
                return false;
            }
        },
        pageRegion() {
            return "/musees/" + this.regionName;
        }
    },
    methods: {

    }
}

</script>


